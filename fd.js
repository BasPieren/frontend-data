var dataFilter = dataArray
  .sort((a, b) => a.YEAR - b.YEAR)
  .map(({ YEAR, PAGES }) => ({ year: YEAR, pages: PAGES }))

var height = 500

var width = 1000

var margin = { top: 20, right: 0, bottom: 30, left: 40 }

var x = d3
  .scaleBand()
  .domain(dataFilter.map(d => d.year))
  .range([margin.left, width - margin.right])
  .padding(1)

var y = d3
  .scaleLinear()
  .domain([0, d3.max(dataFilter, d => d.pages)])
  .nice()
  .range([height - margin.bottom, margin.top])

var xAxis = g =>
  g
    .attr('transform', `translate(0,${height - margin.bottom})`)
    .call(d3.axisBottom(x).tickSizeOuter(0))
    .call(g =>
      g
        .append('text')
        .attr('x', width - margin.right)
        .attr('y', -4)
        .attr('fill', '#000')
        .attr('font-weight', 'bold')
        .attr('text-anchor', 'end')
        .text('Year of publication')
    )

var yAxis = g =>
  g
    .attr('transform', `translate(${margin.left},0)`)
    .call(
      d3.axisLeft(y).tickValues(
        d3
          .scaleLinear()
          .domain(y.domain())
          .ticks()
      )
    )
    .call(g =>
      g
        .selectAll('.tick line')
        .clone()
        .attr('stroke-opacity', 0.1)
        .attr('x2', width - margin.left - margin.right)
    )
    .call(g =>
      g
        .select('.tick:last-of-type text')
        .clone()
        .attr('x', 4)
        .attr('text-anchor', 'start')
        .attr('font-weight', 'bold')
        .text('Number of pages')
    )

var dotColor = d3
  .scaleLinear()
  .domain([
    0,
    d3.median(dataFilter, d => d.pages),
    d3.max(dataFilter, d => d.pages)
  ])
  .nice()
  .range(['green', 'yellow', 'red'])

var svg = d3.select('svg')

svg
  .attr('width', width)
  .attr('height', height)

svg
  .append('g')
  .call(xAxis)
  .attr('stroke-width', 2)

svg.append('g').call(yAxis)

svg
  .append('g')
  .selectAll('circle')
  .data(dataFilter)
  .enter()
  .append('circle')
  .attr('cx', d => x(d.year))
  .attr('cy', d => y(d.pages))
  .attr('r', 5)
  .attr('fill', d => dotColor(d.pages))
